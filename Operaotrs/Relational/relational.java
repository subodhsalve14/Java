public class relational {
    public static void main(String args[]) {

        int a = 10;
        int b = 10;
        int c = 6;
        int d = 10;

        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(a > c);
        System.out.println(a < c);
        System.out.println(a >= d);
        System.out.println(a <= d);

    }
}
